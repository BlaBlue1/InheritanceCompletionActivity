
public class Book {

    private String author, title;


    public Book() {
        this.author = "";
        this.title = "";
    }
    public Book(String author, String title) {
        this.author = author;
        this.title = title;
    }
    public String getAuthor(){
        return author;
    }
    public String getTitle() {
        return title;

    }




}
